    private void initTable(){
        hotele = bd.pobierzHotele();
        DefaultTableModel model = (DefaultTableModel) tableHotels.getModel();
        for (Hotel hotel: hotele) {
            Object[] row = {hotel.getId(),hotel.getNazwaHotelu(),hotel.getMiasto(), hotel.getLiczbaGwiazdek(),
                hotel.isCalodobowaRecepcja(), hotel.isBasen()};
            
             model.addRow(row);
        }
       
    }
    
    private void clearTable(){
        DefaultTableModel dm = (DefaultTableModel) tableHotels.getModel();
        int rowCount = dm.getRowCount();
        
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
}
    }

